package entity;

public class Rank {
	public int rankId;
	public String rankName;
	public float lowPoint;
	public float highPoint;
	
	
	public Rank() {
	}


	public Rank(int rankId, String rankName, float lowPoint, float highPoint) {
		super();
		this.rankId = rankId;
		this.rankName = rankName;
		this.lowPoint = lowPoint;
		this.highPoint = highPoint;
	}


	
	

}
